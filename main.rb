Reflecting on Principle
1) How are Ruby modules (and similar features from other langs)
   different from a class, in their purpose?

   They are not instantiated and provides additional behavior that
   would not be considered part of the class itself

2) What is LSP?

  Liskov Substitution principle states that an object of a subclass
  is able to fulfill the needs of a function of it's superclass
  a function f(x) is fulfilled where x is a type T just as f(y) where y
  is a type S where type S is a subclass of type T

3) Briefly describe how the template method pattern works.

  By providing a generic structure or form of a pattern, it provides
  flexibility on defining the behavior where it is needed
